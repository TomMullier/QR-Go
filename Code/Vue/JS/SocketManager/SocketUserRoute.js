import UserRoute from "../user_route_list.js";

let socket = io()

/* -------------------------------------------------------------------------- */
/*                               FUNCTIONS EMIT                               */
/* -------------------------------------------------------------------------- */

function getAllUserRoutes(){
    socket.emit("getAllUserRoutes");
}



/* -------------------------------------------------------------------------- */
/*                                  Socket.ON                                 */
/* -------------------------------------------------------------------------- */

socket.on("refreshAllUserRoutes", (routes) => {
    UserRoute.refreshAllUserRoutes(routes)
})


/* -------------------------------------------------------------------------- */
/*                                   EXPORT                                   */
/* -------------------------------------------------------------------------- */
export default{
    getAllUserRoutes
}